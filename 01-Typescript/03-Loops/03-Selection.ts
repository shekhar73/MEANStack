function isEvenOrOddNumbers(No : number) : void
{
    if((No % 2) == 0)
    {
        console.log("The given number is an Even number!!!");
    }
    else
    {
        console.log("The given number is an Odd number!!!");
    }
}

isEvenOrOddNumbers(18);
isEvenOrOddNumbers(45);
isEvenOrOddNumbers(44);
isEvenOrOddNumbers(73);